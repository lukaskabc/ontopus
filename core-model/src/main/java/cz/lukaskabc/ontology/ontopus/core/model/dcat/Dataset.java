package cz.lukaskabc.ontology.ontopus.core.model.dcat;

import cz.cvut.kbss.jopa.model.annotations.MappedSuperclass;
import cz.cvut.kbss.jopa.model.annotations.OWLObjectProperty;
import cz.lukaskabc.ontology.ontopus.core.generated.Vocabulary;
import cz.lukaskabc.ontology.ontopus.core.model.id.EntityIdentifier;
import cz.lukaskabc.ontology.ontopus.core.model.util.DocumentedOWLClass;
import java.net.URI;
import java.util.Set;

/**
 * A collection of data, published or curated by a single source, and available for access or download in one or more
 * representations.
 */
@MappedSuperclass
@DocumentedOWLClass(iri = Vocabulary.s_c_dcat_Dataset)
public abstract class Dataset<ID extends EntityIdentifier> extends Resource<ID> {
    @OWLObjectProperty(iri = Vocabulary.s_p_dcat_inSeries)
    private URI series;

    @OWLObjectProperty(iri = Vocabulary.s_p_dcat_distribution)
    private Set<URI> distributions;
    /*
     * Skipping frequency, spatial/geographical coverage, spatial resolution,
     * temporal coverage, temporal resolution, was generated by
     */

    public Set<URI> getDistributions() {
        return distributions;
    }

    public URI getSeries() {
        return series;
    }

    public void setDistributions(Set<URI> distributions) {
        this.distributions = distributions;
    }

    public void setSeries(URI series) {
        this.series = series;
    }
}
