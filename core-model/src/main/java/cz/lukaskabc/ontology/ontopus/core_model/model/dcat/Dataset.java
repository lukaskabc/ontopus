package cz.lukaskabc.ontology.ontopus.core_model.model.dcat;

import cz.cvut.kbss.jopa.model.annotations.MappedSuperclass;
import cz.cvut.kbss.jopa.model.annotations.OWLObjectProperty;
import cz.lukaskabc.ontology.ontopus.core_model.generated.Vocabulary;
import cz.lukaskabc.ontology.ontopus.core_model.model.id.EntityIdentifier;
import cz.lukaskabc.ontology.ontopus.core_model.model.id.VersionSeriesURI;
import cz.lukaskabc.ontology.ontopus.core_model.model.util.DocumentedOWLClass;

import java.net.URI;
import java.util.Set;

/**
 * A collection of data, published or curated by a single source, and available for access or download in one or more
 * representations.
 */
@MappedSuperclass
@DocumentedOWLClass(iri = Vocabulary.s_c_dcat_Dataset)
public abstract class Dataset<DistributionIdentifier extends EntityIdentifier, ID extends EntityIdentifier>
        extends Resource<ID> {
    @OWLObjectProperty(iri = Vocabulary.s_p_dcat_inSeries)
    private URI series;

    /*
     * Skipping distributions (left for impl class) Skipping frequency,
     * spatial/geographical coverage, spatial resolution, temporal coverage,
     * temporal resolution, was generated by
     */

    public abstract Set<DistributionIdentifier> getDistributions();

    public VersionSeriesURI getSeries() {
        return new VersionSeriesURI(series);
    }

    public abstract void setDistributions(Set<DistributionIdentifier> distributions);

    public void setSeries(VersionSeriesURI series) {
        this.series = series.toURI();
    }
}
